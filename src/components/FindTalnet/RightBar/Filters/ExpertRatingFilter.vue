<script setup>
import { ref } from 'vue'
import { useRoute , useRouter } from 'vue-router'

const router = useRouter()
const route = useRoute()
const expert = ref(false)

const updateQueryParam = (key, value) => {
  const query = { ...route.query };
  
  if (value) {
    query[key] = +value;
  } else {
    delete query[key];
  }
  
  return query;
}
const changeRoute = () =>{
  setTimeout(() => {
    const query = updateQueryParam("expert_rating" , expert.value)
    router.push({
      name : 'talents',
